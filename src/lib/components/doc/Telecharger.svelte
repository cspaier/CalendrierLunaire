<h3 class="text-xl font-semibold">Export en SVG</h3>

<p>Vous pouvez télécharger le rendu du calendrier au format SVG en y inserant, si vous le souhaitez, un qr-code dirigeant vers <code>spaier.fr/lune</code>.</p>
<p>En imprimant les visibilités moyennes, vous obtenez un calendrier lunaire perpétuel à afficher au dessus de votre bureau ou dans la chambre de vos enfants.</p>